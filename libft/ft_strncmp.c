#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned long	i;
	int				diff;

	i = 0;
	while (i < n)
	{
		if (s1[i] == '\0' && s2[i] == '\0')
			return (0);
		diff = (unsigned char) s1[i] - (unsigned char) s2[i];
		if (diff != 0)
			return (diff);
		i++;
	}
	return (0);
}
